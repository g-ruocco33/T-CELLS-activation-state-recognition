def plot_learning_curve(model, X, y, title):
    """
    Compute and display the learning curve of a model.

    The learning curve shows how training and validation accuracy evolve
    as we increase the training set size. It helps to understand if the
    model is underfitting or overfitting.

    Parameters
    ----------
    model : sklearn estimator or Pipeline
        The model to evaluate.
    X : np.ndarray
        Feature matrix.
    y : np.ndarray
        Labels.
    title : str
        Plot title.
    """
    train_sizes, train_scores, val_scores = learning_curve(
        model,
        X,
        y,
        cv=5,
        scoring="accuracy",
        train_sizes=np.linspace(0.1, 1.0, 5),
        n_jobs=-1,
        shuffle=True,
        random_state=42,
    )

    train_mean = train_scores.mean(axis=1)
    train_std = train_scores.std(axis=1)
    val_mean = val_scores.mean(axis=1)
    val_std = val_scores.std(axis=1)

    plt.figure(figsize=(6, 4))
    plt.plot(train_sizes, train_mean, "o-", label="train", color="tab:blue")
    plt.fill_between(
        train_sizes,
        train_mean - train_std,
        train_mean + train_std,
        alpha=0.2,
        color="tab:blue",
    )
    plt.plot(train_sizes, val_mean, "o-", label="validation", color="tab:orange")
    plt.fill_between(
        train_sizes,
        val_mean - val_std,
        val_mean + val_std,
        alpha=0.2,
        color="tab:orange",
    )
    plt.xlabel("Number of training samples")
    plt.ylabel("Accuracy")
    plt.title(title)
    plt.legend()
    plt.grid(True)
    plt.tight_layout()
    plt.show()

