def segment_image_cpsam(img_path, mu, sigma):
    """
    Segment a brightfield T-cell image using Cellpose-SAM followed by
    morphological cleanup and watershed splitting of large blobs.

    Parameters
    ----------
    img_path : str
        Full path to the input image (brightfield, single channel).
    mu : float
        Mean used for intensity normalization.
    sigma : float
        Std used for intensity normalization.

    Returns
    -------
    img_gray : np.ndarray (H, W), uint8
        Original grayscale image.
    masks_cp : np.ndarray (H, W), int32
        Label image of segmented cells: 0 = background, 1..N = cell labels.
    img_eq : np.ndarray (H, W), uint8
        Preprocessed and equalized image used for feature extraction.

    Processing steps
    ----------------
    1. Load image in grayscale.
    2. Remove smooth background (unsharp mask) to enhance cell edges.
    3. Apply CLAHE for local contrast enhancement.
    4. Normalize intensities using (pixel - mu) / sigma.
    5. Run Cellpose-SAM on the normalized image.
    6. Remove very small objects and smooth edges via morphological opening.
    7. For large connected components, apply watershed on distance map
       to split touching cells into separate labels.
    """
    # 1) Load grayscale image
    img_gray = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
    if img_gray is None:
        raise FileNotFoundError(f"Image not found or unreadable: {img_path}")

    # 2) Background removal (unsharp mask)
    img_f = img_gray.astype(np.float32)
    blur = cv2.GaussianBlur(img_f, (0, 0), 4)
    img_bg_removed = cv2.addWeighted(img_f, 1.9, -blur, 0.5, 0)

    # 3) Local contrast enhancement (CLAHE)
    img_bg_removed = np.clip(img_bg_removed, 0, 255).astype(np.uint8)
    clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8, 8))
    img_eq = clahe.apply(img_bg_removed)

    # 4) Normalization using dataset mean and std
    img_norm = (img_eq.astype(np.float32) - mu) / (sigma + 1e-8)

    # 5) Cellpose-SAM segmentation (monochannel image -> channels=[0,0])
    masks_cp, flows, styles = cp_model.eval(
        img_norm,
        channels=[0, 0]
    )

    # 6) Morphological cleanup:
    #    - remove small objects (noise)
    #    - smooth edges using morphological opening
    masks_cp = remove_small_objects(masks_cp.astype(bool), min_size=3000)
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (3, 3))
    masks_cp = cv2.morphologyEx(
        masks_cp.astype(np.uint8),
        cv2.MORPH_OPEN,
        kernel
    )

    # 7) Watershed-based splitting of large blobs to separate touching cells
    lab0 = label(masks_cp.astype(bool))  # initial labels of connected components
    lab_final = np.zeros_like(lab0, dtype=np.int32)
    current_label = 1

    for r in regionprops(lab0):
        # mask of the current connected component (blob)
        blob_mask = (lab0 == r.label)

        # If the blob is not too large, keep it as a single cell
        if r.area < 18000:  # threshold may need tuning for your images
            lab_final[blob_mask] = current_label
            current_label += 1
            continue

        # For large blobs, compute distance transform
        distance = ndi.distance_transform_edt(blob_mask)

        # Find local maxima as potential cell centers
        coords = peak_local_max(
            distance,
            footprint=np.ones((50, 50)),  # minimum separation between centers
            labels=blob_mask
        )

        # Build marker image for watershed
        local_max = np.zeros_like(distance, dtype=bool)
        if coords.size > 0:
            local_max[coords[:, 0], coords[:, 1]] = True
        markers = ndi.label(local_max)[0]

        # Apply watershed to split the blob into multiple labels
        ws = watershed(-distance, markers, mask=blob_mask)

        # Assign new labels to each watershed region
        for k in np.unique(ws)[1:]:  # skip background 0
            lab_final[ws == k] = current_label
            current_label += 1

    masks_cp = lab_final  # final labeled mask

    return img_gray, masks_cp, img_eq

