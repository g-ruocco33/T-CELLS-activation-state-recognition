def build_good_cells_dataframe_from_folder(
    images_dir,
    mu,
    sigma,
    goodbad_log_model,
    goodbad_rf_model,
    good_threshold=0.75,
    feature_list=None,
):
    """
    Segment all images in a folder, compute cell features, and keep only
    cells predicted as "good" by the ensemble of good/bad models.

    Parameters
    ----------
    images_dir : str
        Path to the folder containing the NEW brightfield images to process.
    mu, sigma : float
        Mean and std used for intensity normalization (same as training).
    goodbad_log_model : sklearn Pipeline
        Trained Logistic Regression pipeline for good/bad.
    goodbad_rf_model : sklearn Pipeline
        Trained Random Forest pipeline for good/bad.
    good_threshold : float, default=0.75
        Threshold on the combined probability to classify a cell as "good".
    feature_list : list of str, optional
        Names of feature columns used by the quality models. If None,
        use the global feature_cols defined earlier.

    Returns
    -------
    df_good_new : pandas.DataFrame
        DataFrame with one row per "good" cell, including features and
        metadata (filename, path, etc.).

    Notes
    -----
    - This function does NOT classify categories (Naive/Activated/Exhausted).
      It only keeps high-quality cells.
    - You typically call this first, then apply category models to df_good_new.
    """
    if feature_list is None:
        feature_list = feature_cols

    all_rows = []

    for fname in os.listdir(images_dir):
        if not fname.lower().endswith((".png", ".jpg", ".jpeg", ".tif", ".tiff")):
            continue

        img_path = os.path.join(images_dir, fname)
        print(f"[New folder] Processing image: {img_path}")

        # Segment and compute features
        img_gray, masks_cp, img_eq = segment_image_cpsam(img_path, mu, sigma)
        df_img = extract_cell_features(img_gray, masks_cp, img_eq)

        # Add minimal metadata for traceability
        df_img["filename"] = fname
        df_img["path"] = img_path

        if df_img.empty:
            continue

        # Matrix of features for quality model
        X_new = df_img[feature_list].values

        # Good/bad probabilities from both models
        prob_log = goodbad_log_model.predict_proba(X_new)[:, 1]
        prob_rf = goodbad_rf_model.predict_proba(X_new)[:, 1]
        prob_comb = 0.5 * prob_log + 0.5 * prob_rf

        # Decide if each cell is "good"
        df_img["p_good_comb"] = prob_comb
        df_img["pred_good"] = (prob_comb >= good_threshold).astype(int)
        df_img["label_good"] = df_img["pred_good"].map({0: "bad", 1: "good"})

        all_rows.append(df_img)

    if len(all_rows) == 0:
        print("[New folder] No cells found or no valid images.")
        return pd.DataFrame()

    df_all_new = pd.concat(all_rows, ignore_index=True)
    df_good_new = df_all_new[df_all_new["label_good"] == "good"].copy()

    print(f"[New folder] Total cells: {df_all_new.shape[0]}, good cells: {df_good_new.shape[0]}")
    return df_good_new


def run_full_pipeline_on_folder(
    images_dir,
    output_excel_path,
    mu,
    sigma,
    goodbad_log_model,
    goodbad_rf_model,
    cat_log_model,
    cat_rf_model,
    good_threshold=0.75,
    feature_list=None,
):
    """
    Run the full pipeline on a new folder of images:
    1. Segment images and compute features.
    2. Use good/bad ensemble to filter high-quality cells.
    3. Use category models to classify each good cell as Naive/Activated/Exhausted.
    4. Save the final DataFrame to an Excel file.

    Parameters
    ----------
    images_dir : str
        Path to the folder containing the NEW brightfield images.
    output_excel_path : str
        Full path to the Excel file where results will be saved.
    mu, sigma : float
        Mean and std for intensity normalization (same as training).
    goodbad_log_model : sklearn Pipeline
        Trained Logistic Regression pipeline for good/bad.
    goodbad_rf_model : sklearn Pipeline
        Trained Random Forest pipeline for good/bad.
    cat_log_model : sklearn Pipeline
        Trained Logistic Regression pipeline for category classification.
    cat_rf_model : sklearn Pipeline
        Trained Random Forest pipeline for category classification.
    good_threshold : float, default=0.75
        Threshold on the combined good/bad probability.
    feature_list : list of str, optional
        Feature names used by models. If None, uses global feature_cols.

    Returns
    -------
    df_results : pandas.DataFrame
        Final DataFrame with only good cells, including:
        - all features,
        - p_good_comb, label_good,
        - category predictions: logreg, RF, ensemble.

    Usage
    -----
    1. Load trained models from disk:
         goodbad_log_model = log_model
         goodbad_rf_model  = rf_model
         cat_log_model     = joblib.load(model_cat_logreg_path)
         cat_rf_model      = joblib.load(model_cat_rf_path)

    2. Call the function:
         run_full_pipeline_on_folder(
             images_dir=NEW_IMAGES_DIR_1,
             output_excel_path=os.path.join(STATS_DIR, "new_folder_results.xlsx"),
             mu=mu,
             sigma=sigma,
             goodbad_log_model=goodbad_log_model,
             goodbad_rf_model=goodbad_rf_model,
             cat_log_model=cat_log_model,
             cat_rf_model=cat_rf_model,
             good_threshold=0.75,
         )
    """
    if feature_list is None:
        feature_list = feature_cols

    # Step 1-2: segment, extract features, filter good cells
    df_good_new = build_good_cells_dataframe_from_folder(
        images_dir=images_dir,
        mu=mu,
        sigma=sigma,
        goodbad_log_model=goodbad_log_model,
        goodbad_rf_model=goodbad_rf_model,
        good_threshold=good_threshold,
        feature_list=feature_list,
    )

    if df_good_new.empty:
        print("[Full pipeline] No good cells found. Nothing to classify.")
        return df_good_new

    # Step 3: category classification on good cells
    X_new_good = df_good_new[feature_list].values

    proba_log_cat = cat_log_model.predict_proba(X_new_good)
    proba_rf_cat = cat_rf_model.predict_proba(X_new_good)

    # Individual predictions
    pred_log_cat = cat_log_model.predict(X_new_good)
    pred_rf_cat = cat_rf_model.predict(X_new_good)

    # Ensemble of category probabilities
    proba_ensemble_cat = 0.5 * proba_log_cat + 0.5 * proba_rf_cat
    pred_idx_ensemble_cat = proba_ensemble_cat.argmax(axis=1)
    ensemble_classes = cat_log_model.classes_
    pred_ensemble_cat = ensemble_classes[pred_idx_ensemble_cat]

    # Store predictions in the DataFrame
    df_good_new["pred_category_logreg"] = pred_log_cat
    df_good_new["pred_category_rf"] = pred_rf_cat
    df_good_new["pred_category_ensemble"] = pred_ensemble_cat

    # Step 4: save to Excel
    os.makedirs(os.path.dirname(output_excel_path), exist_ok=True)
    df_good_new.to_excel(output_excel_path, index=False)

    print(f"[Full pipeline] Saved results to: {output_excel_path}")
    return df_good_new

