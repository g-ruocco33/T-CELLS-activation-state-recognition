def organize_crops_good_bad(
    df,
    label_col="label_comb",  # or "pred_comb" if you want 0/1
    base_label_is_prob=False,
):
    """
    Move raw cell crops into "good" and "bad" subfolders based on a label.

    Parameters
    ----------
    df : pandas.DataFrame
        DataFrame containing at least:
        - 'crop_raw_path': path to the raw crop,
        - label_col: either "good"/"bad" or 0/1.
    label_col : str, optional
        Column name containing labels used to split cells.
    base_label_is_prob : bool, optional
        If True, interpret label_col as 0/1 and map to "good"/"bad".

    Returns
    -------
    df_out : pandas.DataFrame
        Copy of df with an updated "crop_path" column indicating the
        final path of each crop (in "good" or "bad" subfolder).

    Notes
    -----
    - For each crop:
      1) Determine its label ("good" or "bad").
      2) Move it into the corresponding subfolder inside its original
         "crops_raw" directory.
    """
    df_out = df.copy()

    if "crop_path" not in df_out.columns:
        df_out["crop_path"] = None

    for idx, row in df_out.iterrows():
        src = row.get("crop_raw_path")
        if not isinstance(src, str) or not os.path.exists(src):
            continue

        lab = row.get(label_col)
        if base_label_is_prob:
            lab = "good" if lab == 1 else "bad"
        else:
            lab = str(lab).lower()

        if lab not in ["good", "bad"]:
            continue

        # Original crops_raw directory (e.g. .../Naive/crops_raw)
        cropsraw_dir = os.path.dirname(src)
        good_dir = os.path.join(cropsraw_dir, "good")
        bad_dir = os.path.join(cropsraw_dir, "bad")

        os.makedirs(good_dir, exist_ok=True)
        os.makedirs(bad_dir, exist_ok=True)

        dst_dir = good_dir if lab == "good" else bad_dir

        fname = os.path.basename(src)
        dst = os.path.join(dst_dir, fname)

        shutil.move(src, dst)
        df_out.at[idx, "crop_path"] = dst

    return df_out

