def build_dataset_for_category(img_dir, category_name, mu, sigma):
    """
    Segment all images in a category folder, extract cell features,
    and save raw crops for each cell.

    Parameters
    ----------
    img_dir : str
        Path to the folder containing original images of one category
        (e.g. ".../T-CELLS_DATASET/Naive").
    category_name : str
        Name of the category ("Naive", "Activated", "Exhausted").
    mu, sigma : float
        Mean and std used for intensity normalization.

    Returns
    -------
    df_cat : pandas.DataFrame
        DataFrame with one row per cell, including:
        - all features from extract_cell_features
        - filename, path, category
        - crop_raw_path: path to the raw cell crop saved on disk

    Notes
    -----
    - For each input image, we:
      1) Segment cells.
      2) Extract features.
      3) Save each cell as a separate crop in a "crops_raw" subfolder.
    """
    rows = []

    # Folder where raw crops for this category will be stored
    crops_root = os.path.join(img_dir, "crops_raw")
    os.makedirs(crops_root, exist_ok=True)

    # Loop over all image files in the category folder
    for fname in os.listdir(img_dir):
        if not fname.lower().endswith((".png", ".jpg", ".jpeg", ".tif", ".tiff")):
            continue

        path = os.path.join(img_dir, fname)

        # Segment image and compute features
        img_gray, masks_cp, img_eq = segment_image_cpsam(path, mu, sigma)
        df_img = extract_cell_features(img_gray, masks_cp, img_eq)

        # Add metadata columns
        df_img["filename"] = fname
        df_img["path"] = path
        df_img["category"] = category_name

        # Build dictionary of regions for cropping
        lab = label(masks_cp)
        regions = {r.label: r for r in regionprops(lab)}

        crop_paths = []
        base_name, _ = os.path.splitext(fname)

        # For each cell (row in df_img), create and save a crop
        for _, row in df_img.iterrows():
            cell_id = int(row["label"])

            if cell_id not in regions:
                # In case the label is missing for some reason, keep empty string
                crop_paths.append("")
                continue

            r = regions[cell_id]
            minr, minc, maxr, maxc = r.bbox
            crop = img_gray[minr:maxr, minc:maxc]

            # Build crop filename and full path
            crop_name = f"{category_name}_{base_name}_id{cell_id}.tif"
            crop_path = os.path.join(crops_root, crop_name)

            # Save crop to disk
            cv2.imwrite(crop_path, crop)

            crop_paths.append(crop_path)

        # Add crop paths to the DataFrame
        df_img["crop_raw_path"] = crop_paths

        rows.append(df_img)

    if len(rows) == 0:
        # If no images or no cells, return an empty DataFrame
        return pd.DataFrame()

    # Concatenate all per-image DataFrames into one for this category
    df_cat = pd.concat(rows, ignore_index=True)
    return df_cat

