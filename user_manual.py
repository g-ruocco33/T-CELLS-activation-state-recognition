def compute_dataset_mean_std(training_image_paths):
    """
    Compute mean and standard deviation of pixel intensities over a set of
    brightfield images, after basic preprocessing (background removal and
    local contrast enhancement).

    Parameters
    ----------
    training_image_paths : list of str
        List of full paths to images used only for estimating normalization.

    Returns
    -------
    mu : float
        Global mean of preprocessed pixel intensities.
    sigma : float
        Global standard deviation of preprocessed pixel intensities.

    Rationale
    ---------
    We want Cellpose-SAM to see images that are normalized in a consistent
    way across the dataset. We therefore:
    - convert to grayscale,
    - remove smooth background with an "unsharp mask" style filter,
    - apply CLAHE (local contrast enhancement),
    then we compute mean and std over all processed pixels.
    """
    pixels = []  # will store flattened pixel arrays from each processed image

    for img_path in training_image_paths:
        # 1) Load image in grayscale
        img_gray = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
        if img_gray is None:

            continue

        # 2) Background removal (unsharp mask)
        img_f = img_gray.astype(np.float32)
        blur = cv2.GaussianBlur(img_f, (0, 0), 4)
        img_bg_removed = cv2.addWeighted(img_f, 1.9, -blur, 0.5, 0)             # You can adjust these values if needed

        # Clip to valid 8-bit range and convert back to uint8
        img_bg_removed = np.clip(img_bg_removed, 0, 255).astype(np.uint8)

        # 3) Local contrast enhancement (CLAHE)
        clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8, 8))
        img_eq = clahe.apply(img_bg_removed)

        # 4) Store all pixels from this processed image
        pixels.append(img_eq.flatten())

    if len(pixels) == 0:
        raise RuntimeError("No valid images found in training_path for mean/std computation.")

    # 5) Concatenate all pixels and compute statistics
    all_pixels = np.concatenate(pixels)
    mu = float(np.mean(all_pixels))
    sigma = float(np.std(all_pixels))

    print(f"[Normalization] Dataset mean: {mu:.2f}")
    print(f"[Normalization] Dataset std:  {sigma:.2f}")

    return mu, sigma


